package bekaku.android.forum.model;

import java.io.Serializable;

public class PostModel implements Serializable {

    private int postId = 0;
    private String content;
    private String created;
    private int threadId = 0;
    private int userAccountId = 0;
    private String userAccountName;
    private String userAccountPiture;
    private int voteUp = 0;
    private int voteDown = 0;

    public int getPostId() {
        return postId;
    }

    public void setPostId(int postId) {
        this.postId = postId;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getCreated() {
        return created;
    }

    public void setCreated(String created) {
        this.created = created;
    }

    public int getThreadId() {
        return threadId;
    }

    public void setThreadId(int threadId) {
        this.threadId = threadId;
    }

    public int getUserAccountId() {
        return userAccountId;
    }

    public void setUserAccountId(int userAccountId) {
        this.userAccountId = userAccountId;
    }

    public String getUserAccountName() {
        return userAccountName;
    }

    public void setUserAccountName(String userAccountName) {
        this.userAccountName = userAccountName;
    }

    public String getUserAccountPiture() {
        return userAccountPiture;
    }

    public void setUserAccountPiture(String userAccountPiture) {
        this.userAccountPiture = userAccountPiture;
    }

    public int getVoteUp() {
        return voteUp;
    }

    public void setVoteUp(int voteUp) {
        this.voteUp = voteUp;
    }

    public int getVoteDown() {
        return voteDown;
    }

    public void setVoteDown(int voteDown) {
        this.voteDown = voteDown;
    }
}
